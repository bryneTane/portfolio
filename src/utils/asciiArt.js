/**
 * Generate ASCII art banner from text
 * @param {string} text - Text to convert (uppercase recommended)
 * @returns {string} - Multi-line ASCII art string
 */
export function generateAsciiArt(text) {
  const upperText = text.toUpperCase();
  const lines = ['', '', '', '', '', ''];

  for (let i = 0; i < upperText.length; i++) {
    const char = upperText[i];
    const charArt = asciiFont[char];

    if (charArt) {
      for (let lineIndex = 0; lineIndex < 6; lineIndex++) {
        lines[lineIndex] += charArt[lineIndex];
      }
    } else {
      // Unknown character, use space
      for (let lineIndex = 0; lineIndex < 6; lineIndex++) {
        lines[lineIndex] += '   ';
      }
    }
  }

  return '\n' + lines.join('\n') + '\n';
}

/**
 * React component wrapper for ASCII art
 * @param {string} text - Text to display
 * @param {object} style - Optional inline styles
 */
export function AsciiArtBanner({ text, style = {} }) {
  const art = generateAsciiArt(text);

  return (
    <pre
      style={{
        color: 'var(--term-orange)',
        fontSize: '10px',
        lineHeight: 1,
        marginBottom: '40px',
        overflow: 'hidden',
        textShadow: '0 0 10px rgba(246, 139, 72, 0.3)',
        ...style,
      }}
    >
      {art}
    </pre>
  );
}
